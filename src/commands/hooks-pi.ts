/**
 * Remove Mulch extension from Pi.
 */
async function uninstallMulchExtension(): Promise<void> {
	const extensionPath = join(getPiExtensionsDir(), "mulch-pi-extension.ts");
	const settingsPath = getPiSettingsPath();

	// Remove extension file
	if (existsSync(extensionPath)) {
		await unlink(extensionPath);
		printSuccess("Removed Mulch extension");
	} else {
		printWarning("No Mulch extension found", "nothing to remove");
	}

	// Update settings.json
	if (existsSync(settingsPath)) {
		try {
			const content = await readFile(settingsPath, "utf-8");
			const settings = JSON.parse(content) as PiSettings;

			if (settings.extensions) {
				const beforeLength = settings.extensions.length;
				settings.extensions = settings.extensions.filter(
					ext => !ext.includes("mulch")
				);

				if (settings.extensions.length < beforeLength) {
					await writeFile(settingsPath, JSON.stringify(settings, null, "\t"), "utf-8");
					printSuccess("Updated Pi settings");
				}
			}
		} catch (err) {
			printWarning("Failed to update settings.json", String(err));
		}
	}
}

/**
 * Show Mulch extension installation status.
 */
async function statusMulchExtension(json: boolean): Promise<void> {
	const cwd = process.cwd();
	const config = await loadConfig(cwd);
	const projectRoot = config.project.root;

	const sourcePath = join(projectRoot, "mulch", "pi-extension", "mulch-pi-extension.ts");
	const extensionPath = join(getPiExtensionsDir(), "mulch-pi-extension.ts");
	const settingsPath = getPiSettingsPath();

	const sourceExists = await Bun.file(sourcePath).exists();
	const extensionExists = existsSync(extensionPath);

	let settingsHasExtension = false;

	if (existsSync(settingsPath)) {
		try {
			const content = await readFile(settingsPath, "utf-8");
			const settings = JSON.parse(content) as PiSettings;

			if (settings.extensions) {
				settingsHasExtension = settings.extensions.some(
					ext => ext.includes("mulch")
				);
			}
		} catch {
			// Ignore parse errors
		}
	}

	if (json) {
		jsonOutput("mulch status", {
			sourceExists,
			extensionExists,
			settingsHasExtension,
		});
	} else {
		console.log(`Mulch extension source: ${sourceExists ? "✓ present" : "✗ missing"}`);
		console.log(`Mulch extension installed: ${extensionExists ? "✓ yes" : "✗ no"}`);
		console.log(`Pi settings.json: ${existsSync(settingsPath) ? "✓ exists" : "✗ missing"}`);
		console.log(`Extension in settings: ${settingsHasExtension ? "✓ yes" : "✗ no"}`);

		if (!extensionExists && sourceExists) {
			printHint("");
			printHint("Run ov hooks-pi install-mulch");
		}
	}
}

