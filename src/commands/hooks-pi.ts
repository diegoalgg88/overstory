/**
 * Remove Seeds extension from Pi.
 */
async function uninstallSeedsExtension(): Promise<void> {
	const extensionPath = join(getPiExtensionsDir(), "seeds-pi-extension.ts");
	const settingsPath = getPiSettingsPath();

	// Remove extension file
	if (existsSync(extensionPath)) {
		await unlink(extensionPath);
		printSuccess("Removed Seeds extension");
	} else {
		printWarning("No Seeds extension found", "nothing to remove");
	}

	// Update settings.json
	if (existsSync(settingsPath)) {
		try {
			const content = await readFile(settingsPath, "utf-8");
			const settings = JSON.parse(content) as PiSettings;

			if (settings.extensions) {
				const beforeLength = settings.extensions.length;
				settings.extensions = settings.extensions.filter(
					ext => !ext.includes("seeds")
				);

				if (settings.extensions.length < beforeLength) {
					await writeFile(settingsPath, JSON.stringify(settings, null, "\t"), "utf-8");
					printSuccess("Updated Pi settings");
				}
			}
		} catch (err) {
			printWarning("Failed to update settings.json", String(err));
		}
	}
}

