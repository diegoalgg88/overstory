#!/usr/bin/env bun

/**
 * CLI command: ov hooks-pi install|uninstall|status
 *
 * Manages orchestrator hooks for Pi (Google ADK).
 * Pi uses TypeScript extensions with factory functions - NOT shell commands.
 * 
 * According to Pi architecture analysis:
 * - Extensions are TypeScript modules with default export factory function
 * - Located in ~/.pi/agent/extensions/ or specified in settings.json
 * - 25+ lifecycle events available (tool_call, session_start, etc.)
 * - Can block tools via { block: true, reason: "..." }
 * - Event bus for inter-extension communication
 */

import { existsSync } from "node:fs";
import { mkdir, readFile, unlink, writeFile } from "node:fs/promises";
import { join } from "node:path";
import { homedir } from "node:os";
import { Command } from "commander";
import { loadConfig } from "../config.ts";
import { ValidationError } from "../errors.ts";
import { jsonOutput } from "../json.ts";
import { printHint, printSuccess, printWarning } from "../logging/color.ts";

interface HookEntry {
	matcher: string;
	hooks: ReadonlyArray<{ type: string; command: string }>;
}

interface OverstoryHooksConfig {
	hooks: Record<string, HookEntry[]>;
}

interface PiSettings {
	extensions?: string[];
	packages?: string[];
	skills?: string[];
	prompts?: string[];
	themes?: string[];
}

/**
 * Get Pi agent directory path.
 */
function getPiAgentDir(): string {
	return join(homedir(), ".pi", "agent");
}

/**
 * Get Pi extensions directory.
 */
function getPiExtensionsDir(): string {
	return join(getPiAgentDir(), "extensions");
}

/**
 * Get Pi settings.json path.
 */
function getPiSettingsPath(): string {
	return join(getPiAgentDir(), "settings.json");
}

/**
 * Generate TypeScript extension for Pi.
 * 
 * Pi extensions use factory functions with ExtensionAPI.
 * This creates a comprehensive Overstory integration extension.
 */
function generatePiExtension(hooks: OverstoryHooksConfig): string {
	// Map Overstory events to Pi events
	const eventMappings = generateEventMappings(hooks);
	
	return `/**
 * Overstory Integration Extension for Pi (Google ADK)
 * 
 * Auto-generated by \`ov hooks-pi install\`
 * 
 * This extension integrates Overstory multi-agent orchestration with Pi,
 * providing mail injection, tool interception, logging, and session management.
 */

import type { ExtensionAPI, ExtensionContext } from "@mariozechner/pi-coding-agent";

// Overstory state management
interface OverstoryState {
	sessionInitialized: boolean;
	lastMailCheck: number;
	toolCallCount: Map<string, number>;
}

const state: OverstoryState = {
	sessionInitialized: false,
	lastMailCheck: 0,
	toolCallCount: new Map(),
};

/**
 * Execute Overstory command asynchronously.
 * Non-blocking - fires and forgets.
 */
async function runOvCommand(cmd: string, ctx?: ExtensionContext): Promise<void> {
	try {
		const { exec } = await import("child_process");
		const { promisify } = await import("util");
		const execAsync = promisify(exec);
		
		await execAsync(cmd, { 
			timeout: 30000,
			env: {
				...process.env,
				PI_PROJECT_DIR: process.cwd(),
			}
		});
	} catch (error) {
		// Silently ignore - don't disrupt Pi
		if (ctx) {
			ctx.logger?.debug?.("Overstory command failed", error);
		}
	}
}

/**
 * Extract tool name from tool call event.
 */
function getToolName(eventName: string, input: unknown): string {
	if (eventName === "tool_call") {
		const event = input as { toolName?: string };
		return event.toolName || "unknown";
	}
	return "unknown";
}

/**
 * Check if tool should be blocked.
 */
function shouldBlockTool(toolName: string, input: unknown): { block: boolean; reason?: string } {
	// Block dangerous git operations
	if (toolName === "bash") {
		const bashInput = input as { command?: string };
		const command = bashInput.command || "";
		
		if (command.includes("git push")) {
			// Log but don't block (user can override)
			runOvCommand("ov log git-push-attempted --agent orchestrator");
			return { 
				block: false, // Don't block, just warn
				reason: "git push detected - ensure you've merged locally first"
			};
		}
		
		if (command.includes("rm -rf /") || command.includes("sudo rm -rf")) {
			return { 
				block: true, 
				reason: "Dangerous command blocked by Overstory"
			};
		}
	}
	
	return { block: false };
}

/**
 * Get mail context for injection.
 */
async function getMailContext(): Promise<string> {
	try {
		const { exec } = await import("child_process");
		const { promisify } = await import("util");
		const execAsync = promisify(exec);
		
		const result = await execAsync("ov mail check --inject --agent orchestrator", {
			timeout: 10000
		});
		
		return result.stdout?.trim() || "No new messages";
	} catch {
		return "Mail check unavailable";
	}
}

/**
 * Initialize Overstory session.
 */
async function initializeSession(ctx: ExtensionContext): Promise<void> {
	if (state.sessionInitialized) return;
	
	state.sessionInitialized = true;
	await runOvCommand("ov prime --agent orchestrator", ctx);
	
	ctx.ui?.notify?.("Overstory initialized", "info");
}

/**
 * Cleanup Overstory session.
 */
async function cleanupSession(ctx: ExtensionContext): Promise<void> {
	await runOvCommand("ov log session-end --agent orchestrator", ctx);
	await runOvCommand("mulch learn", ctx);
	
	state.sessionInitialized = false;
}

/**
 * Main extension factory function.
 */
export default function (pi: ExtensionAPI) {
	const logger = {
		log: (msg: string) => console.log("[Overstory]", msg),
		debug: (msg: string, ...args: unknown[]) => console.debug("[Overstory]", msg, ...args),
		warn: (msg: string) => console.warn("[Overstory]", msg),
		error: (msg: string, err?: unknown) => console.error("[Overstory]", msg, err),
	};

	// ========================================
	// SESSION LIFECYCLE
	// ========================================

	/**
	 * Session Start - Initialize Overstory
	 */
	pi.on("session_start", async (_event, ctx) => {
		logger.log("Session started");
		await initializeSession(ctx);
		
		// Inject initial context
		const mailContext = await getMailContext();
		ctx.ui?.notify?.(mailContext, "info");
	});

	/**
	 * Session Shutdown - Cleanup
	 */
	pi.on("session_shutdown", async (_event, ctx) => {
		logger.log("Session shutting down");
		await cleanupSession(ctx);
	});

	// ========================================
	// USER PROMPT INTERCEPTION
	// ========================================

	/**
	 * Input - Check for new mail on user prompt
	 */
	pi.on("input", async (event, ctx) => {
		const now = Date.now();
		const MAIL_CHECK_INTERVAL = 5 * 60 * 1000; // 5 minutes
		
		if (now - state.lastMailCheck > MAIL_CHECK_INTERVAL) {
			state.lastMailCheck = now;
			const mailContext = await getMailContext();
			
			if (mailContext && mailContext !== "No new messages") {
				// Inject as context before agent starts
				pi.events.emit("overstory:mail_context", { context: mailContext });
			}
		}
	});

	/**
	 * Before Agent Start - Inject mail context
	 */
	pi.on("before_agent_start", async (event, ctx) => {
		// Check for mail context from input handler
		return new Promise((resolve) => {
			const mailHandler = (data: { context: string }) => {
				resolve({
					message: {
						customType: "overstory-mail",
						content: data.context,
						display: false, // Don't show to user
					}
				});
			};
			
			pi.events.once("overstory:mail_context", mailHandler);
			
			// Timeout after 2 seconds
			setTimeout(() => resolve({}), 2000);
		});
	});

	// ========================================
	// TOOL INTERCEPTION
	// ========================================

	/**
	 * Tool Call - Intercept and validate
	 */
	pi.on("tool_call", async (event, ctx) => {
		const toolName = event.toolName;
		const toolInput = event.input;
		
		// Track tool usage
		const count = state.toolCallCount.get(toolName) || 0;
		state.toolCallCount.set(toolName, count + 1);
		
		// Log tool start
		runOvCommand(\`ov log tool-start --agent orchestrator --tool-name $\{toolName}\`, ctx);
		
		// Check if should block
		const blockResult = shouldBlockTool(toolName, toolInput);
		if (blockResult.block) {
			logger.warn(\`Blocked tool: $\{toolName} - $\{blockResult.reason}\`);
			return { 
				block: true, 
				reason: blockResult.reason 
			};
		}
		
		if (blockResult.reason) {
			ctx.ui?.notify?.(blockResult.reason, "warning");
		}
	});

	/**
	 * Tool Result - Log completion
	 */
	pi.on("tool_result", async (event, ctx) => {
		const toolName = event.toolName;
		
		// Log tool end
		runOvCommand(\`ov log tool-end --agent orchestrator --tool-name $\{toolName}\`, ctx);
		
		// Auto-record learnings after git commit
		if (toolName === "bash") {
			const bashInput = event.input as { command?: string };
			if (bashInput?.command?.includes("git commit")) {
				runOvCommand("mulch diff HEAD~1", ctx);
			}
		}
	});

	// ========================================
	// CONTEXT MANAGEMENT
	// ========================================

	/**
	 * Context - Modify messages before LLM
	 */
	pi.on("context", async (event, ctx) => {
		// Could inject additional context here if needed
		// For now, we rely on before_agent_start
	});

	/**
	 * Before Compact - Prime context
	 */
	pi.on("session_before_compact", async (_event, ctx) => {
		await runOvCommand("ov prime --agent orchestrator --compact", ctx);
	});

	// ========================================
	// CUSTOM TOOLS (Optional)
	// ========================================

	/**
	 * Register Overstory status tool
	 */
	pi.registerTool({
		name: "overstory_status",
		label: "Overstory Status",
		description: "Check Overstory multi-agent orchestration status",
		parameters: {} as any, // Using Typebox would be better
		async execute(_toolCallId, _params, _signal, _onUpdate, ctx) {
			try {
				const { exec } = await import("child_process");
				const { promisify } = await import("util");
				const execAsync = promisify(exec);
				
				const result = await execAsync("ov status --json", { timeout: 10000 });
				const status = JSON.parse(result.stdout);
				
				return {
					content: [
						{ 
							type: "text", 
							text: \`Overstory Status:\\n$\{JSON.stringify(status, null, 2)}\` 
						}
					],
					details: {},
				};
			} catch (error) {
				return {
					content: [
						{ 
							type: "text", 
							text: \`Overstory status check failed: $\{error}\` 
						}
					],
					details: { error: String(error) },
					isError: true,
				};
			}
		},
	});

	/**
	 * Register mail check tool
	 */
	pi.registerTool({
		name: "overstory_mail",
		label: "Check Overstory Mail",
		description: "Check for new messages from other Overstory agents",
		parameters: {} as any,
		async execute(_toolCallId, _params, _signal, _onUpdate, ctx) {
			const mailContext = await getMailContext();
			
			return {
				content: [
					{ type: "text", text: mailContext }
				],
				details: {},
			};
		},
	});

	// ========================================
	// EVENT BUS INTEGRATION
	// ========================================

	/**
	 * Listen for custom Overstory events
	 */
	pi.events.on("overstory:checkpoint", (data) => {
		logger.debug("Checkpoint event received", data);
	});

	/**
	 * Emit session metrics
	 */
	pi.events.on("agent_end", () => {
		// Emit metrics for this turn
		pi.events.emit("overstory:turn_metrics", {
			toolCalls: Object.fromEntries(state.toolCallCount),
			timestamp: Date.now(),
		});
	});

	logger.log("Overstory extension loaded");
}
`;
}

/**
 * Generate event mappings from Overstory hooks.
 */
function generateEventMappings(hooks: OverstoryHooksConfig): string {
	const mappings: string[] = [];
	
	for (const [ovEvent, entries] of Object.entries(hooks.hooks)) {
		for (const entry of entries) {
			for (const hook of entry.hooks) {
				if (hook.command.includes("ov mail")) {
					mappings.push(`${ovEvent} → input/before_agent_start`);
				} else if (hook.command.includes("ov log tool-start")) {
					mappings.push(`${ovEvent} → tool_call`);
				} else if (hook.command.includes("ov log tool-end")) {
					mappings.push(`${ovEvent} → tool_result`);
				} else if (hook.command.includes("ov prime")) {
					mappings.push(`${ovEvent} → session_start/session_before_compact`);
				}
			}
		}
	}
	
	return mappings.join(", ");
}

/**
 * Install Overstory hooks for Pi.
 */
async function installHooksPi(force: boolean): Promise<void> {
	const cwd = process.cwd();
	const config = await loadConfig(cwd);
	const projectRoot = config.project.root;

	// Read source hooks from .overstory/hooks.json
	const sourcePath = join(projectRoot, ".overstory", "hooks.json");
	const sourceFile = Bun.file(sourcePath);
	if (!(await sourceFile.exists())) {
		throw new ValidationError("No hooks.json found in .overstory/. Run 'ov init' first.", {
			field: "source",
		});
	}

	const sourceContent = await sourceFile.text();
	const sourceHooks = JSON.parse(sourceContent) as OverstoryHooksConfig;

	// Generate Pi extension
	const extensionCode = generatePiExtension(sourceHooks);

	// Get paths
	const piExtensionsDir = getPiExtensionsDir();
	const extensionPath = join(piExtensionsDir, "overstory-integration.ts");
	const settingsPath = getPiSettingsPath();

	// Create extensions directory if needed
	await mkdir(piExtensionsDir, { recursive: true });

	// Check if extension already exists
	if (!force && existsSync(extensionPath)) {
		console.log("Overstory extension already installed for Pi.");
		console.log("Use --force to overwrite.");
		return;
	}

	// Write extension
	await writeFile(extensionPath, extensionCode, "utf-8");
	
	printSuccess("Created Overstory extension");
	printHint(`  ${extensionPath}`);

	// Update settings.json to include extension
	let settings: PiSettings = {};
	
	if (existsSync(settingsPath)) {
		try {
			const content = await readFile(settingsPath, "utf-8");
			settings = JSON.parse(content) as PiSettings;
		} catch {
			if (force) {
				printWarning("Existing settings.json is invalid, will create new");
			} else {
				throw new ValidationError("settings.json exists but is invalid. Use --force to overwrite.", {
					field: "settings",
				});
			}
		}
	}

	// Add extension path if not already present
	if (!settings.extensions) {
		settings.extensions = [];
	}

	// Use relative path with ~ instead of absolute path for portability
	const relativeExtensionPath = "~/.pi/agent/extensions/overstory-integration.ts";

	// Remove any absolute paths to this extension (cleanup from previous installs)
	settings.extensions = settings.extensions.filter(
		ext => !ext.includes("overstory-integration.ts")
	);

	// Add the relative path
	settings.extensions.push(relativeExtensionPath);

	await writeFile(settingsPath, JSON.stringify(settings, null, "\t"), "utf-8");
	printSuccess("Updated Pi settings");
	printHint(`  ${settingsPath}`);

	printHint("");
	printHint("Installed Pi events:");
	printHint("  - session_start → ov prime");
	printHint("  - input → ov mail check (periodic)");
	printHint("  - before_agent_start → inject mail context");
	printHint("  - tool_call → ov log tool-start + validation");
	printHint("  - tool_result → ov log tool-end");
	printHint("  - session_shutdown → ov log session-end + mulch learn");
	printHint("  - session_before_compact → ov prime --compact");
	printHint("");
	printHint("Registered tools:");
	printHint("  - overstory_status");
	printHint("  - overstory_mail");
	printHint("");
	printHint("Restart Pi for changes to take effect.");
}

